function fmtAddr(p) {
  const parts = [];
  const city = p.city || p.town || p.village || p.county;
  if (city) parts.push(city);
  if (p.state && p.state !== city) parts.push(p.state);
  if (p.country) parts.push(p.country);
  return parts.join(', ');
}

