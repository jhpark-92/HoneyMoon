function ensureHotelInDay(day) {
  const h = hotelOfDay(day);
  const hotelItemId = `hotel_${h.id}`;
  const arr = state.itin[day] || [];
  if (arr.some(p => p.id === hotelItemId)) return;
  const others = arr.filter(p => p.type !== 'hotel');
  state.itin[day] = [{
    id: hotelItemId,
    name: h.name,
    lat: h.lat,
    lng: h.lng,
    addr: h.city,
    type: 'hotel',
    emoji: h.emoji,
  }, ...others];
}

