// 별점 → 별 이모지
function starStr(rating) {
  if (!rating) return '';
  const full  = Math.floor(rating);
  const half  = (rating - full) >= 0.5 ? '½' : '';
  return '⭐'.repeat(full) + half + ` ${rating.toFixed(1)}`;
}

