// 터키어 특수문자 포함 소문자 변환 (İ→i, Ş→ş, Ğ→ğ, Ü→ü, Ö→ö, Ç→ç)
function toNormalLower(str) {
  return str
    .replace(/İ/g, 'i').replace(/I/g, 'i')
    .replace(/Ş/g, 'ş').replace(/Ğ/g, 'ğ')
    .replace(/Ü/g, 'ü').replace(/Ö/g, 'ö').replace(/Ç/g, 'ç')
    .toLowerCase();
}

function migrateNames(itin) {
  for (let d = 1; d <= TOTAL_DAYS; d++) {
    if (!Array.isArray(itin[d])) continue;
    itin[d].forEach(pl => {
      // 이름 한국어 변환 (터키어 특수문자 포함 대소문자 무관)
      if (pl.name) {
        const key = toNormalLower(pl.name);
        const mapped = NAME_KO_MAP[key];
        if (mapped) pl.name = mapped;
      }
      // osmValue(카테고리)가 없으면 이름 기반으로 보정 (osmKey 무관)
      if (!pl.osmValue && pl.name) {
        const cat = NAME_CAT_MAP[pl.name];
        if (cat) { pl.osmKey = '_google'; pl.osmValue = cat; }
      }
    });
  }
}

function applyParsed(p) {
  const itin = p.itin || p;
  migrateNames(itin);
  for (let d = 1; d <= TOTAL_DAYS; d++) {
    if (Array.isArray(itin[d])) state.itin[d] = itin[d];
  }
  if (p.flights) {
    if (p.flights[5])  Object.assign(state.flights[5],  p.flights[5]);
    if (p.flights[7])  Object.assign(state.flights[7],  p.flights[7]);
    if (p.flights[11]) Object.assign(state.flights[11], p.flights[11]);
  }
  if (p.notes) Object.assign(state.notes, p.notes);
}

async function loadState() {
  if (SHARED_DATA) {
    applyParsed(SHARED_DATA);
    for (let d = 1; d <= TOTAL_DAYS; d++) ensureHotelInDay(d);
    return;
  }

  // QR 스캔으로 들어온 경우: ?script=URL 파라미터로 자동 등록
  const urlScript = new URLSearchParams(location.search).get('script');
  if (urlScript) {
    setScriptUrl(decodeURIComponent(urlScript));
    history.replaceState(null, '', location.pathname);
  }

  // API 키는 항상 Apps Script에서만 가져오도록 localStorage 캐시 제거
  localStorage.removeItem(GOOGLE_KEY_KEY);

  // 1순위: 클라우드 (Apps Script URL이 있으면 자동 로드)
  const cloud = await cloudLoad();
  if (cloud) {
    applyParsed(cloud);
    for (let d = 1; d <= TOTAL_DAYS; d++) ensureHotelInDay(d);
    // 로컬에도 캐시
    localStorage.setItem('honeymoon-turkey-2026', JSON.stringify(cloud));
    return;
  }

  // 3순위: localStorage (오프라인 폴백)
  try {
    const s = localStorage.getItem('honeymoon-turkey-2026');
    if (s) applyParsed(JSON.parse(s));
  } catch (_) {}

  for (let d = 1; d <= TOTAL_DAYS; d++) ensureHotelInDay(d);
}

function encodeData(json) {
  // LZString으로 압축 후 URL 해시에 안전한 base64로 인코딩
  if (typeof LZString !== 'undefined') {
    return 'z' + LZString.compressToEncodedURIComponent(json);
  }
  return btoa(unescape(encodeURIComponent(json)));
}

function decodeData(hash) {
  if (hash.startsWith('z') && typeof LZString !== 'undefined') {
    return LZString.decompressFromEncodedURIComponent(hash.slice(1));
  }
  // 구버전 호환 (압축 없이 저장된 데이터)
  return decodeURIComponent(escape(atob(hash)));
}

