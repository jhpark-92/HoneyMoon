const KO_WD = ['일','월','화','수','목','금','토'];

function dayDate(day) {
  const d = new Date(TRIP_START);
  d.setDate(d.getDate() + day - 1);
  return d;
}

function fmtDay(day) {
  const d = dayDate(day);
  return `7/${d.getDate()} (${KO_WD[d.getDay()]})`;
}

