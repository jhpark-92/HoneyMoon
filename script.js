function bezierArc(from, to, steps = 22) {
  const mx = (from[0] + to[0]) / 2 + 1.2;
  const my = (from[1] + to[1]) / 2;
  const pts = [];
  for (let i = 0; i <= steps; i++) {
    const t = i / steps;
    pts.push([
      (1-t)**2 * from[0] + 2*(1-t)*t * mx + t**2 * to[0],
      (1-t)**2 * from[1] + 2*(1-t)*t * my + t**2 * to[1],
    ]);
  }
  return pts;
}

