function buildTransportGuide() {
  let html = '';
  const CITY_TIPS = {
    istanbul:   '이스탄불카트로 T1 트램·지하철 이용 가능',
    cappadocia: '렌터카 or 현지 투어 차량 추천',
    antalya:    '칼레이치 주변은 도보권 / 원거리는 택시',
  };

  for (let day = 1; day <= TOTAL_DAYS; day++) {
    const places = state.itin[day] || [];
    if (places.length < 2) continue;

    const city    = cityOfDay(day);
    const color   = DAY_COLORS[day - 1];
    const cityTip = CITY_TIPS[city];
    let numMap = {}, n = 0;
    places.forEach((pl, i) => { if (pl.type !== 'hotel') numMap[i] = ++n; });

    html += `<div class="tg-day">
      <div class="tg-day-header">
        <span class="tg-day-dot" style="background:${color}"></span>
        <strong>Day ${day}</strong>
        <span class="tg-day-date">${fmtDay(day)}</span>
      </div>`;

    if (cityTip) {
      html += `<div class="tg-city-tip">💡 ${cityTip}</div>`;
    }

    for (let i = 1; i < places.length; i++) {
      const from = places[i - 1];
      const to   = places[i];
      const km   = dist(from.lat, from.lng, to.lat, to.lng);
      const tr   = getTransport(km, city);
      const fromName = from.type === 'hotel' ? `${from.emoji} 호텔` : `${numMap[i-1]}. ${from.name}`;
      const toName   = to.type   === 'hotel' ? `${to.emoji} 호텔`   : `${numMap[i]}. ${to.name}`;

      html += `
        <div class="tg-leg">
          <div class="tg-leg-from">${esc(fromName)}</div>
          <div class="tg-leg-arrow">
            <span class="tg-leg-icon">${tr.icon}</span>
            <div class="tg-leg-info">
              <span class="tg-leg-mode">${tr.mode}</span>
              ${tr.tip ? `<span class="tg-leg-tip">${tr.tip}</span>` : ''}
            </div>
            <span class="tg-leg-km">${fmtDist(km)}</span>
          </div>
          <div class="tg-leg-to">${esc(toName)}</div>
        </div>`;
    }
    html += `</div>`;
  }

  if (!html) {
    html = '<div class="tg-empty">일정에 장소를 추가하면<br>이동 방법이 자동으로 생성돼요 🗺️</div>';
  }
  return html;
}

function initNotes() {
  const btn   = document.getElementById('notesBtn');
  const panel = document.getElementById('notesPanel');
  const close = document.getElementById('notesPanelClose');
  if (!btn || !panel) return;

  const textarea  = document.getElementById('notesTextarea');
  const guideDiv  = document.getElementById('notesTransportGuide');

  let activeTab = NOTE_SECTIONS[0].id;

  function renderNotesPanel() {
    const tabsHtml = NOTE_SECTIONS.map(s => `
      <button class="notes-tab${s.id === activeTab ? ' active' : ''}" data-id="${s.id}">
        ${s.icon} ${s.title}
      </button>`).join('');
    document.getElementById('notesTabs').innerHTML = tabsHtml;

    const sec = NOTE_SECTIONS.find(s => s.id === activeTab);

    if (sec.autoGenerated) {
      textarea.style.display  = 'none';
      guideDiv.style.display  = 'block';
      guideDiv.innerHTML      = buildTransportGuide();
    } else {
      textarea.style.display  = '';
      guideDiv.style.display  = 'none';
      textarea.value          = state.notes[activeTab] || '';
      textarea.placeholder    = sec.placeholder;
    }

    document.querySelectorAll('.notes-tab').forEach(t => {
      t.addEventListener('click', () => {
        activeTab = t.dataset.id;
        renderNotesPanel();
      });
    });
  }

  // 항공·이동 탭 최초 1회 자동 채우기
  if (!state.notes.flight) {
    state.notes.flight = buildDefaultFlightNote();
    saveState();
  }

  function openNotes() {
    panel.classList.add('open');
    renderNotesPanel();
  }
  btn.addEventListener('click', openNotes);

  // 모바일 탭 영역 버튼
  const tabsNotesBtn = document.getElementById('tabsNotesBtn');
  if (tabsNotesBtn) tabsNotesBtn.addEventListener('click', openNotes);
  close.addEventListener('click', () => panel.classList.remove('open'));
  panel.addEventListener('click', e => { if (e.target === panel) panel.classList.remove('open'); });

  // 자동 저장 (500ms 디바운스)
  let saveTimer = null;
  document.getElementById('notesTextarea').addEventListener('input', e => {
    state.notes[activeTab] = e.target.value;
    clearTimeout(saveTimer);
    saveTimer = setTimeout(saveState, 500);
  });
}

document.addEventListener('DOMContentLoaded', init);
