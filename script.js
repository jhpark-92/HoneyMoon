let enrichToken = 0;

// 해당 날짜 장소 정보를 백그라운드에서 미리 로드
async function preloadDayEnrichment(day) {
  if (!getGoogleKey()) return;
  const places = (state.itin[day] || []).filter(p => p.type !== 'hotel');
  for (const pl of places) {
    const cacheKey = `${pl.lat.toFixed(4)},${pl.lng.toFixed(4)}`;
    if (enrichCache[cacheKey]) continue; // 이미 캐시됨
    const info = await fetchPlaceInfo(pl);
    if (info) {
      enrichCache[cacheKey] = {
        desc:        info.description || '',
        imageUrl:    info.imageUrl    || null,
        rating:      info.rating      || null,
        reviewCount: info.reviewCount || null,
      };
    }
    await new Promise(r => setTimeout(r, 300)); // 요청 간격 (rate limit 방지)
  }
}

async function fetchPlaceInfo(pl) {
  const key = getGoogleKey();
  if (!key) return null;
  try {
    const body = {
      textQuery: pl.name,
      locationBias: { circle: { center: { latitude: pl.lat, longitude: pl.lng }, radius: 2000 } },
      languageCode: 'ko',
      maxResultCount: 1,
    };
    const res = await fetch(`https://places.googleapis.com/v1/places:searchText?key=${encodeURIComponent(key)}`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'X-Goog-FieldMask': 'places.photos,places.editorialSummary,places.rating,places.userRatingCount',
      },
      body: JSON.stringify(body),
    });
    const data = await res.json();
    const place = data.places?.[0];
    if (!place) return null;

    const photoName = place.photos?.[0]?.name;
    return {
      imageUrl:    photoName ? `https://places.googleapis.com/v1/${photoName}/media?maxWidthPx=800&key=${key}` : null,
      description: place.editorialSummary?.text || null,
      rating:      place.rating || null,
      reviewCount: place.userRatingCount || null,
    };
  } catch (_) { return null; }
}

