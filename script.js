function getTransport(km, city) {
  const walkMin = Math.round(km / 4 * 60);
  const taxiMin = Math.round(km / 20 * 60); // 시내 교통 감안

  if (km < 0.6) {
    return { icon: '🚶', mode: `도보 약 ${walkMin}분`, tip: null };
  }
  if (km < 1.5) {
    return { icon: '🚶', mode: `도보 약 ${walkMin}분`, tip: '택시 이용 시 5분 내' };
  }
  if (city === 'istanbul') {
    if (km < 4) {
      return { icon: '🚕', mode: `택시 약 ${taxiMin}~${taxiMin + 5}분`, tip: 'T1 트램 이용 가능 (이스탄불카트)' };
    }
    if (km < 10) {
      return { icon: '🚕', mode: `택시 약 ${taxiMin}~${taxiMin + 10}분`, tip: '트램·지하철 환승 추천' };
    }
    return { icon: '🚕', mode: `택시 약 ${taxiMin}분+`, tip: '우버·비탁시 앱 추천' };
  }
  if (city === 'cappadocia') {
    if (km < 5) return { icon: '🚕', mode: `택시 약 ${taxiMin}분`, tip: '렌터카 이용 시 편리' };
    return { icon: '🚗', mode: `택시·렌터카 약 ${taxiMin}분`, tip: '투어 차량 이용 추천' };
  }
  if (city === 'antalya') {
    if (km < 3) return { icon: '🚕', mode: `택시 약 ${taxiMin}분`, tip: '칼레이치 근처는 도보권' };
    return { icon: '🚕', mode: `택시 약 ${taxiMin}분`, tip: 'AntalyaRay 트램 이용 가능' };
  }
  return { icon: '🚕', mode: `택시 약 ${taxiMin}분`, tip: null };
}

function matchedHotels(q) {
  const lower = q.toLowerCase();
  return HOTELS.filter(h =>
    h.name.toLowerCase().includes(lower) ||
    h.city.toLowerCase().includes(lower) ||
    (h.aliases || []).some(a => a.toLowerCase().includes(lower))
  );
}

async function doSearch(q, container) {
  const drop = container || document.getElementById('searchDropdown');
  drop.innerHTML = '<div class="sr-status">🔍 검색 중...</div>';
  if (!container) drop.classList.add('open');

  try {
    // ── 구글 API 키가 있으면 Google Places 우선 ──
    if (getGoogleKey()) {
      // 한국어 → 영어/터키어 번역 후 검색 (Google은 영어 쿼리에 더 정확)
      const gQuery  = translateQuery(q);
      let   gPlaces = await googlePlacesSearch(gQuery);
      // 번역 쿼리 결과 없으면 원문으로 재시도
      if (gPlaces !== null && gPlaces.length === 0 && gQuery !== q) {
        gPlaces = await googlePlacesSearch(q) ?? [];
      }
      // 결과 있으면 표시, 없으면 Photon 폴백으로 계속 진행
      if (gPlaces !== null && gPlaces.length > 0) {
        renderGoogleResults(drop, q, gPlaces);
        return;
      }
    }

    // ── 폴백: Photon ──
    const translated = translateQuery(q);
    const geojson    = await photonFetch(translated);
    const features   = geojson.features || [];

    matchedHotels(q).forEach(h => {
      features.unshift({
        properties: { name: h.name, city: h.city, country: '튀르키예', osm_key: 'tourism', osm_value: 'hotel' },
        geometry: { coordinates: [h.lng, h.lat] },
      });
    });

    if (!features.length) {
      drop.innerHTML = `<div class="sr-status">검색 결과가 없어요 😔<br><small>예: 그랜드 바자르, 아야소피아, Göreme</small></div>`;
      return;
    }

    drop.innerHTML = '';
    features.forEach(feat => {
      const p     = feat.properties;
      const name  = p.name || p.street || '';
      const addr  = fmtAddr(p);
      const ico   = placeIcon(p.osm_key, p.osm_value);
      const label = categoryLabel(p.osm_key, p.osm_value);
      const color = categoryColor(p.osm_key);
      const [lng, lat] = feat.geometry.coordinates;

      const div = document.createElement('div');
      div.className = 'sr-item';
      div.innerHTML = `
        <span class="sr-icon">${ico}</span>
        <div class="sr-text">
          <div class="sr-name">${esc(name)}</div>
          <div class="sr-meta">
            ${label ? `<span class="sr-badge" style="background:${color}">${label}</span>` : ''}
            <span class="sr-addr">${esc(addr)}</span>
          </div>
        </div>`;
      div.addEventListener('click', () =>
        addPlace({ name, lat, lng, addr, osmKey: p.osm_key, osmValue: p.osm_value })
      );
      drop.appendChild(div);
    });
  } catch (_) {
    drop.innerHTML = '<div class="sr-status">검색 오류가 발생했어요 😢<br><small>인터넷 연결을 확인해주세요</small></div>';
  }
}

